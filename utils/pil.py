from PIL import Image


def test_pil_create_new():
    width = 400
    height = 300
    img = Image.new(mode='RGB', size= (width, height))
    img.show()


def test_pil_get_info():
    im = Image.open('python.png')
    print(im.size)
    print(im.info)
    print(im.mode)
    print(im.format)
    print(im.format_description)

